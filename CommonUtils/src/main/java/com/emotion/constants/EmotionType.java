package com.emotion.constants;

/**
 * @author VISHWAS
 * Enum class for Emotions
 */
public enum EmotionType {
	EMOTION_HAPPY("1","HAPPY"),
	EMOTION_SURPRISE("2","SURPRISE"),
	EMOTION_SORRY("3","SORRY"),
	EMOTION_ANGRY("4","ANGRY"),
	EMOTION_DISGUST("5","DISGUST"),
	EMOTION_BLUE("6","BLUE"),
	EMOTION_SAD("7","SAD"),
	EMOTION_EMBARRASSED("8","EMBARRASSED"),
	EMOTION_HURT("9","HURT");	


	String id;
	String value;

	private EmotionType(String id, String value){
		this.id = id;
		this.value = value;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

};