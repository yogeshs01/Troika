package com.emotion.domainObject;

import java.util.List;


public class UserDetails {

	private int id;
	private String userName;
	private String password;
	private String userType;
	private String emailId;
	private List<Integer>emotionIdList;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUserType() {
		return userType;
	}
	public void setUserType(String userType) {
		this.userType = userType;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public List<Integer> getEmotionIdList() {
		return emotionIdList;
	}
	public void setEmotionIdList(List<Integer> emotionIdList) {
		this.emotionIdList = emotionIdList;
	}
	
}
